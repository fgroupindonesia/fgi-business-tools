    private void renderPreview() {

        if (radio_candy.isSelected()) {
            labelLinkPreview.setIcon(candyIcon);
        } else if (radio_simple.isSelected()) {
            labelLinkPreview.setIcon(simpleIcon);
        } else if (radio_vip.isSelected()) {
            labelLinkPreview.setIcon(vipIcon);
        }

    }

