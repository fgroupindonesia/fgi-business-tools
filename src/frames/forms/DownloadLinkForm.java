    public void refresh() {

        comboboxAppsProfile.removeAllItems();

        ArrayList<AppsProfile> data = db.select_all_AppProfile();
        for (AppsProfile ap : data) {
            comboboxAppsProfile.addItem(ap.getName() + " ver : " + ap.getVersion() + " platform type : " + ap.getPlatform_Type());
        }
    }

    public void setData(DownloadLink dataNa) {
        if (dataNa != null) {
            // mode edit
            editMode = true;
            dataTemp = dataNa;
        }

        UIForm.setComboboxData(dataNa.getApp_name(), comboboxAppsProfile);
        UIForm.setRadioButtonData(dataNa.getLink_mode(), radio_candy, radio_simple, radio_vip);
        textfield_directURL.setText(dataNa.getLast_download());

        labelLastDownload.setText(dataNa.getLast_download());
        labelTotalDownload.setText(dataNa.getTotal_overall() + " user download.");
    }

