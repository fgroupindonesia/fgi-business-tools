    private void button_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_saveActionPerformed

        AppsProfile data = new AppsProfile();

        if (!editMode) {
            db.insert_AppProfile(data);
        } else {

            db.update_AppProfile(data);
        }

        dispose();

    }//GEN-LAST:event_button_saveActionPerformed

    private void comboboxAppsProfileItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboboxAppsProfileItemStateChanged

        if (evt.getStateChange() == ItemEvent.SELECTED) {
            // request on POST to server
            renderPreview();
        }

    }//GEN-LAST:event_comboboxAppsProfileItemStateChanged

