    private void labelNotificationMouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_labelNotificationMouseEntered

        // lets check whether there's available notification (0) mark or not?
        if (labelNotification.getText().contains("(")) {
            labelNotification.setIcon(yellow_mailClosed);
        } else {
            labelNotification.setIcon(yellow_mailOpened);
        }

    }//GEN-LAST:event_labelNotificationMouseEntered

    private void labelNotificationMouseExited(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_labelNotificationMouseExited
        if (labelNotification.getText().contains("(")) {
            labelNotification.setIcon(blue_mailClosed);
        } else {
            labelNotification.setIcon(blue_mailOpened);
        }
    }//GEN-LAST:event_labelNotificationMouseExited

