    public void updateCheckWork() {

        UserSettings user = db.select_specific_UserSettings(1);

        UpdateChecker work = new UpdateChecker();

        work.checkWhen(work.asOptions(user.getCheck_updates()));

        if (!work.isTurned_off() && work.isUpdates_ready()) {

            int res = PopupMessage.showConfirmation("New update is ready. Do you want to download now?");
            if (res == JOptionPane.YES_OPTION) {

                work.downloadNow();
                
            } 
        }

    }

