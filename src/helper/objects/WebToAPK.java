
package helper.objects;

/**
 *
 * @author staff
 */
public class WebToAPK {
    private int id;
    private String appname;
    private String url;
    private String submitted_date;
    private String queue_id;
    private String status;
    private String completed_date;
    private String download_link;

    /**
     * @return the appname
     */
    public String getAppname() {
        return appname;
    }

    /**
     * @param appname the appname to set
     */
    public void setAppname(String appname) {
        this.appname = appname;
    }

    /**
     * @return the url
     */
    public String getUrl() {
        return url;
    }

    /**
     * @param url the url to set
     */
    public void setUrl(String url) {
        this.url = url;
    }

    /**
     * @return the submitted_date
     */
    public String getSubmitted_date() {
        return submitted_date;
    }

    /**
     * @param submitted_date the submitted_date to set
     */
    public void setSubmitted_date(String submitted_date) {
        this.submitted_date = submitted_date;
    }

    /**
     * @return the queue_id
     */
    public String getQueue_id() {
        return queue_id;
    }

    /**
     * @param queue_id the queue_id to set
     */
    public void setQueue_id(String queue_id) {
        this.queue_id = queue_id;
    }

    /**
     * @return the status
     */
    public String getStatus() {
        return status;
    }

    /**
     * @param status the status to set
     */
    public void setStatus(String status) {
        this.status = status;
    }

    /**
     * @return the completed_date
     */
    public String getCompleted_date() {
        return completed_date;
    }

    /**
     * @param completed_date the completed_date to set
     */
    public void setCompleted_date(String completed_date) {
        this.completed_date = completed_date;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the download_link
     */
    public String getDownload_link() {
        return download_link;
    }

    /**
     * @param download_link the download_link to set
     */
    public void setDownload_link(String download_link) {
        this.download_link = download_link;
    }
}
